export function useMongoI18n() {
  const locale = ref(localStorage.getItem('mongodb_visual_locale') || 'zh')

  const setLocale = (value: string) => {
    locale.value = value
    localStorage.setItem('mongodb_visual_locale', value)
  }

  const t = computed(() => messages[locale.value as keyof typeof messages] || messages.zh)

  return {
    locale,
    setLocale,
    t,
    languages: [
      { label: '中文', value: 'zh' },
      { label: 'English', value: 'en' },
    ],
  }
}
